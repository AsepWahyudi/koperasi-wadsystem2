function loaddata(idload){
	$().ready(function () {
		$("#mymodals").modal("show");
		var urldata	=	base_url + $('.' + idload).attr('var-url');
		$.ajax({
			type: "POST",
			url: urldata,
			data: 'data=',
			cache: false,
				success: function(msg){
					_parse_html(msg);
				}, error: function (result) {
					var teks = result['status'] + " - " + result['statusText'];
					alert(teks);
				}
		});
	});
}

function _parse_html(data){
	var obj = JSON.parse(data);
    $("#nama").val(obj.nama_jaminan);
    $("#idtrx").val(obj.id_jaminan);
}

$('#mymodals').on('hidden.bs.modal', function () {
    $("#nama").val("");
    $("#idtrx").val("");
})